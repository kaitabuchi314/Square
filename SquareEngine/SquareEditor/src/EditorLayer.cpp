void EditorLayer::InputVector(const char* title, const char* id, glm::vec3* vector)
{
    float position[3] = { vector->x, vector->y, vector->z };

    ImGui::Text(title);
    ImGui::SameLine();
    ImGui::InputFloat3(id, position);

    vector->x = position[0];
    vector->y = position[1];
    vector->z = position[2];
}
